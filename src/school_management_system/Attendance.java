    private void aidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_aidKeyReleased

        System.out.println(aid.getText());
        int idit = Integer.parseInt(aid.getText());
        try {
            Class.forName(driver);
            con = DriverManager.getConnection(url, "root", "");
            stm = con.createStatement();
            String sql = "select * from stureg where sid = " + idit;

            rs = stm.executeQuery(sql);
            if (rs.next()) {
                aname.setText(rs.getString("sname"));
                aclass.setText(rs.getString("class"));
                aroll.setText(rs.getString("roll"));

            };
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_aidKeyReleased

