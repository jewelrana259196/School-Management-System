    private void ridActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ridActionPerformed
        DefaultTableModel tm = (DefaultTableModel) attn_table.getModel();
        
        try {
            Class.forName(driver);
            con = DriverManager.getConnection(url, "root", "");
            //int idi = Integer.parseInt(aroll.getText());
            String sql = "select * from attendance where roll = " + rid.getText();
            
            stm = con.createStatement();
            rs = stm.executeQuery(sql);

            while (rs.next()) {
                Object o[] = {rs.getInt("id"), rs.getString("name"), rs.getString("class"), rs.getString("roll"), rs.getString("day"), rs.getString("attendancet")};
                tm.addRow(o);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_ridActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Class.forName(driver);
            con = DriverManager.getConnection(url, "root", "");
            //int idi = Integer.parseInt(aroll.getText());
            String sql = "SELECT name, COUNT(attendancet) from attendance where attendancet like '%absent%' and roll='" + rid.getText()+"'";
            
            stm = con.createStatement();
            
            rs = stm.executeQuery(sql);
            while (rs.next()) {
                rs.getString("name");
                rs.getString("COUNT(attendancet)");
                JOptionPane.showMessageDialog(null, "Name: "+rs.getString("name")+"\nAbsent: "+rs.getString("COUNT(attendancet)")+" Days");
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

